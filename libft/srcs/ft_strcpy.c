#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	size_t i;

	if (!src)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
